const container = document.getElementById('container')
const btn = document.getElementById('btn')
const search = document.getElementById('search')

const min = document.getElementById('min')
const max = document.getElementById('max')
const filterbtn = document.getElementById('filter')
const refreshbtn = document.getElementById('refresh')

const mask = document.getElementById('mask')

function RenderItems(array) {
    container.innerHTML = ''

    array.forEach((element, index) => {
        container.innerHTML += `
        <div onclick='openModal'(${index})' class='card'>
            <div class="img">
                <img src="${element.image}" alt="">
            </div>
            <h1>${element.title}</h1>
            <b>${element.description}</b>
            <div class="flex">
                <b>Price: ${element.price}</b>
                <p>${element.currency}</p>
            </div>
            <button>Add to cart</button>
        </div>
        `
    });
}

function searchItems() {
    const filteredArray = []
    data.forEach(element => {
        if (element.title === search.value) {
            filteredArray.push(element)
        }        
    });
    RenderItems(filteredArray)
}
btn.addEventListener('click', searchItems)

function filter() {
    const filteredArray = []
    data.forEach(element => {
        if (element.price >= min.value && element.price <= max.value) {
            filteredArray.push(element)
        }        
    });
    RenderItems(filteredArray)
}
filterbtn.addEventListener('click', filter)

function closeModal() {
    document.body.style.overflow = 'auto'
    mask.style.display = 'none'
}
mask.addEventListener('click', closeModal)

function openModal(index) {
    document.body.style.overflow = 'hidden'
    const obj = data[index]
    mask.style.display = 'block'
    mask.innerHTML = `
        <div id='modal'>
            <div class='flex'>
                <div></div>
                <div onclick='closeModal()'>x</div)
            </div>
            <img src="${obj.image}" alt="">
            <h1>${obj.title}</h1>
            <b>${obj.description}</b>
            <div class="flex">
                <b>Price: ${obj.price}</b>
                <p>${obj.currency}</p>
            </div>
            <br>
            <button>Add to cart</button>
        </div>
        `
}

refreshbtn.addEventListener('click', function() {
    RenderItems(data);
});

RenderItems(data)